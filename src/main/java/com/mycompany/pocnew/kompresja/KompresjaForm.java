    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        JFileChooser fileopen = new JFileChooser();
//        int ret = fileopen.showSaveDialog(null); 
        if (fileopen.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
            String fileToSave = fileopen.getSelectedFile().getAbsolutePath();
            new KompresjaService(parent, new File(fileToSave)).calculate(jSliderJakosc.getValue());
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

    private void jButtonLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadActionPerformed
        JFileChooser fileopen = new JFileChooser();
//        int ret = fileopen.showOpenDialog(null); 
        if (fileopen.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileopen.getSelectedFile();
            new DekompresjaService(fileToSave, jLabelImage);
        }
    }//GEN-LAST:event_jButtonLoadActionPerformed

